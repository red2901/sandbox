        public void InitialiseTimeInYearFractionsAndDiscountFactors()
        {
            this.CurveDates = this.TestVector;
            this.Yields = this.TestVectorResult;
            this.TimeInYearFractions.Clear();
            this.DiscountFactors.Clear();

            var anchorDate = this.CurveDates[0];
            for (int i = 0; i < this.CurveDates.Count; i++)
            {
                var timeInYearFractions = (this.CurveDates[i] - anchorDate) / (double)365.25;
                this.TimeInYearFractions.Add(timeInYearFractions);
                var discountFactor = Math.Exp(-(double)this.Yields[i] * (double)timeInYearFractions / 100);
                this.DiscountFactors.Add((double)discountFactor);
            }            
        }

